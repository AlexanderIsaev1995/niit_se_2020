public class Loops {

    public static void main(String[] args) {


        boolean tr = false;

        while (tr) {
            System.out.println("Цикл");

            tr = false;
        }


        int i = 0;

        do {
            System.out.println("Цикл do while");
            i++;
        } while (i < 2);




    }
}
